class TestCalliope(unittest.TestCase):
    @patch('datetime.datetime')
    def test_battery_should_be_serviced(self, mock_datetime):
        mocked_today = datetime(2023,10,14).date()
        mock_datetime.today().date.return_value = mocked_today

        mock_datetime.today.return_value = date(2025, 1, 1)
        mock_datetime.date.today.return_value = date(2025, 1, 1)

        last_service_date = mocked_today.replace(year=mocked_today.year - 3)
        current_mileage = 0
        last_service_mileage = 0

        car = Calliope(last_service_date, current_mileage, last_service_mileage)
        self.assertTrue(car.engine_should_be_serviced())

    def test_battery_should_not_be_serviced(self):
        today = datetime.today().date()
        last_service_date = today.replace(year=today.year - 1)
        current_mileage = 0
        last_service_mileage = 0

        car = Calliope(last_service_date, current_mileage, last_service_mileage)
        self.assertFalse(car.needs_service())

    def test_engine_should_be_serviced(self):
        last_service_date = datetime.today().date()
        current_mileage = 30001
        last_service_mileage = 0

        car = Calliope(last_service_date, current_mileage, last_service_mileage)
        self.assertTrue(car.engine_should_be_serviced())

    @patch('engine.model.calliope.Calliope.check_engine_health', return_value=True)
    def test_engine_should_not_be_serviced(self, mock_check_engine_health):

        last_service_date = datetime.today().date()
        current_mileage = 30000
        last_service_mileage = 0

        car = Calliope(last_service_date, current_mileage, last_service_mileage)
        self.assertFalse(car.engine_should_be_serviced())




